    private void btn_calculateActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btn_calculateActionPerformed
    {//GEN-HEADEREND:event_btn_calculateActionPerformed
        // TODO add your handling code here:
        
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mysql","root","Thoke@123");
            
            String query = "select sum(total_price) from `medical_store`.`sup_order`";
            PreparedStatement pst = con.prepareStatement(query);
            
            ResultSet rs;
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                txt_total_orders.setText(rs.getString(1));
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Record Not Found!!");
            }
            
            query = "select sum(total_price) from `medical_store`.`bill`";
            pst = con.prepareStatement(query);
            
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                txt_total_selling.setText(rs.getString(1));
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Record Not Found!!");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,"Error "+e);
        }
    }//GEN-LAST:event_btn_calculateActionPerformed

