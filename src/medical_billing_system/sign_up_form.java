    private void btn_sign_upActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btn_sign_upActionPerformed
    {//GEN-HEADEREND:event_btn_sign_upActionPerformed
        // TODO add your handling code here:
        
        if(txt_email_id.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Please Enter Email ID");
              return;
        }
        if(pf_pet_name.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Please Enter Pet Name");
              return;
        }
        if(pf_pwd1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Please Enter Password");
             return;
        }
        if(pf_pwd2.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Please Confirm Password");
             return;
        }
       
        try
        {
            if(pf_pwd1.getText().equals(pf_pwd2.getText()))
            {
               
            }
            else
            {
                 JOptionPane.showMessageDialog(this, "Password did not Matched!!\n Enter password again");
                return;
            }
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mysql","root","Thoke@123");
            
            String query = "select * from `medical_store`.`signup` where email_id=? and pwd=?";
            
            PreparedStatement pst = con.prepareStatement(query);
            
            pst.setString(1, txt_email_id.getText());
            pst.setString(2, pf_pet_name.getText());
            
            ResultSet rs;
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                 JOptionPane.showMessageDialog(this, "Already Account Exists !!\n Login to Continue");
                 return;
            }
    
            query="INSERT INTO `medical_store`.`signup` (`email_id`,`pet_name`, `pwd`) VALUES (?, ?,?)";
            pst = con.prepareStatement(query);
            
            pst.setString(1,txt_email_id.getText());
            pst.setString(2,pf_pet_name.getText());
            pst.setString(3,pf_pet_name.getText());
            
            pst.executeUpdate();
            
           JOptionPane.showMessageDialog(this,"Signed Up Successfully !!"+txt_email_id.getText());
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,"Sign Up Failed ! \nTry Again "+e);
        }
    }//GEN-LAST:event_btn_sign_upActionPerformed

