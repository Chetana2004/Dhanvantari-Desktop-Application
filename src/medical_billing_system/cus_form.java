    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btn_submitActionPerformed
    {//GEN-HEADEREND:event_btn_submitActionPerformed
        // TODO add your handling code here:
        
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mysql","root","Thoke@123");
            
            String query;
            query = "insert into `medical_store`.`customer` (cus_fname,cus_mname,cus_lname,age,gender,contact_no,email_id,address) values (?,?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            
            pst.setString(1,txt_fname.getText());
            pst.setString(2,txt_mname.getText());
            pst.setString(3,txt_lname.getText());
            pst.setString(4,txt_age.getText());
            pst.setString(5, (String) cmb_gender.getSelectedItem());
            pst.setString(6,txt_contact_no.getText());
            pst.setString(7,txt_email_id.getText());
            pst.setString(8,txt_area_address.getText());
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Record Inserted Successfully!!");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,"Record Not Inserted!! "+e);
        }
    }//GEN-LAST:event_btn_submitActionPerformed

