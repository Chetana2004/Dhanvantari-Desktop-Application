    private void btn_fetch_listActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btn_fetch_listActionPerformed
    {//GEN-HEADEREND:event_btn_fetch_listActionPerformed
        // TODO add your handling code here:
        
        try
        {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mysql","root","Thoke@123");
            
            String query = "select  * from `medical_store`.`employee`";
            PreparedStatement pst = con.prepareStatement(query);
      
            ResultSet rs;
            rs = pst.executeQuery();
            
            DefaultTableModel table_model = (DefaultTableModel) tbl_emp.getModel();
          
            String emp_id,fname,mname,lname,emp_role,salary;
            
            while(rs.next())
            {
                emp_id = rs.getString("emp_id");
                fname = rs.getString("fname");
                mname = rs.getString("mname");
                lname = rs.getString("lname");
                emp_role = rs.getString("emp_role");
                salary = rs.getString("salary");
                
                String[] row = {emp_id,fname,mname,lname,emp_role,salary};
                
                table_model.addRow(row);
            }
            
            pst.close();
            con.close();
        }
        catch(Exception e)
        {
               JOptionPane.showMessageDialog(this,"Error :"+e);
        }
    }//GEN-LAST:event_btn_fetch_listActionPerformed

